while counter != 0 and wrong != 6:
    print(img[wrong])
    s = input("Enter letter ---> ")
    if len(s) != 1:
        print("Error")
        wrong+=1
        continue
    
    index = word.find(s)
    if index == -1:
        wrong+=1
    if index != -1 and answer[index] != '_':
        wrong+=1
    while index != -1:
        counter -= 1
        answer[index] = s
        index = word.find(s, index+1)
    print(' '.join(answer))
    
if wrong == 6:
    print("Game over!!!")
    print(img[wrong])
